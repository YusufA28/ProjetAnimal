		Pas d’anesthésie
		Il s’agit d’abord des mutilations. La plus courante est la castration des porcelets. Agés d’à peine une semaine,
		environ 85 % d’entre eux subissent le même sort : ils sont castrés par l’éleveur, afin d’éviter une odeur désagréable
		à la cuisson, dite «odeur de verrat». Inoffensive pour le consommateur, elle n’apparaîtrait que dans 5 % des cas… L
		e cahier des charges bio prévoit que cette mutilation soit pratiquée avec une anesthésie ou une analgésie, comme la
		bombe à froid. «Les porcelets ne bénéficient pas d’anesthésie car l’éleveur ne peut pas la pratiquer lui-même,
		affirme Pauline Di Nicolantonio, coordinatrice à Welfarm, une association de protection des animaux de la ferme.
		Quant aux analgésiques, ils sont très insuffisants pour prendre en charge une telle douleur.» Jean-François Vincent,
		éleveur d’agneaux et de porcs bio dans le Cher, également secrétaire national «viande» de la Fédération nationale
		d’agriculture biologique (Fnab), reconnaît que le recours à la bombe à froid n’est pas «forcément satisfaisant»
		et que la castration peut être vue comme un acte «violent». «Dans les élevages conventionnels, certains utilisent
		des anti-inflammatoires mais nous, en bio, on n’en veut pas, dit-il. La Fnab a commencé à travailler sur des
		alternatives, nous venons de lancer une étude.»

